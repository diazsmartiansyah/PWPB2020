package com.diazs.latihanintent;

public class Music {
    int photo;
    String judul;
    String penyanyi;
    int lagu;

    public int getLagu() {
        return lagu;
    }

    public void setLagu(int lagu) {
        this.lagu = lagu;
    }

    public int getPhoto() {
        return photo;
    }

    public void setPhoto(int photo) {
        this.photo = photo;
    }

    public String getJudul() {
        return judul;
    }

    public void setJudul(String judul) {
        this.judul = judul;
    }

    public String getPenyanyi() {
        return penyanyi;
    }

    public void setPenyanyi(String penyanyi) {
        this.penyanyi = penyanyi;
    }
}
